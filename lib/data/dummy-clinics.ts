export function getDummyClinics(filters?: {
  prefecture?: string
  specialty?: string
  query?: string
  limit?: number
}): Clinic[] {
  let filtered = [...dummyClinics]

  if (filters?.prefecture) {
    filtered = filtered.filter((c) => c.prefecture === filters.prefecture)
  }

  if (filters?.specialty) {
    filtered = filtered.filter((c) =>
      c.featured_subjects?.includes(filters.specialty!)
    )
  }

  if (filters?.query) {
    const query = filters.query.toLowerCase()
    filtered = filtered.filter(
      (c) =>
        c.clinic_name.toLowerCase().includes(query) ||
        c.address.toLowerCase().includes(query) ||
        c.stations?.toLowerCase().includes(query) ||
        c.featured_subjects?.toLowerCase().includes(query)
    )
  }

  if (filters?.limit) {
    filtered = filtered.slice(0, filters.limit)
  }

  return filtered
}
